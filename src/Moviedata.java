    private void bt_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_updateActionPerformed
        // TODO add your handling code here:
        if(bt_update.getText() == "bt_update"){
           txt_judul.requestFocus();
        }else{
            double akting = parseInt(txt_akting.getText());
            double alur = parseInt(txt_alur.getText());
            String judul = txt_judul.getText();
            double penokohan = parseInt(txt_penokohan.getText());
            double nilai = (alur+akting+penokohan)/3;
            
        if(alur > 5.0 || penokohan > 5.0 || akting > 5.0){
               JOptionPane.showMessageDialog(null,"Maksimal semua input adalah 5!"); 
            } else if(alur < 0 || penokohan < 0 || akting < 0){
                JOptionPane.showMessageDialog(null,"Minimal semua input adalah 0!");
            } else{
                    try{
                        Statement statement = (Statement) konek.getConnection().createStatement();
                        statement.executeUpdate("UPDATE `movie` SET `judul` = '"+txt_judul.getText()+"', `alur` = '"+txt_alur.getText()+"', `penokohan` = '"+txt_penokohan.getText()+"'"
                                + ", `akting` = '"+txt_akting.getText()+"', `nilai` = '"+nilai+"' WHERE `movie`.`judul` = '"+judul+"';");
                        statement.close();
                        JOptionPane.showMessageDialog(null, "Data Berhasil diubah");
                        }catch (Exception t){
                        JOptionPane.showMessageDialog(null, "Data Gagal diubah");
                        }
                    }
        datatable();
        }
    }//GEN-LAST:event_bt_updateActionPerformed

