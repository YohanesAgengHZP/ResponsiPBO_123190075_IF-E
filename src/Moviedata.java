    private void bt_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_tambahActionPerformed
        // TODO add your handling code here:
        double akting = parseInt(txt_akting.getText());
        double alur = parseInt(txt_alur.getText());
        String judul = txt_judul.getText();
        double penokohan = parseInt(txt_penokohan.getText());
        double nilai = (alur+akting+penokohan)/3;
        
        if(alur > 5 || penokohan > 5 || akting > 5){
               JOptionPane.showMessageDialog(null,"Maksimal semua input adalah 5!"); 
            } else if(alur < 0 || penokohan < 0 || akting < 0){
                JOptionPane.showMessageDialog(null,"Minimal semua input adalah 0");
            } else{
                try{
                Statement statement = (Statement) konek.getConnection().createStatement();
                statement.executeUpdate("INSERT INTO `movie` (`judul`, `alur`, `penokohan`, `akting`, `nilai`) VALUES ('" + txt_judul.getText() + "', '" + txt_alur.getText() + "', '" + txt_penokohan.getText() + "', '" + txt_akting.getText() + "', '"+ nilai + "')");
                statement.close();
                JOptionPane.showMessageDialog(null, "Data Berhasil disimpan");
                }catch (Exception t){
                    JOptionPane.showMessageDialog(null, "Data sudah ada");
                }
            }
    datatable();  
    }//GEN-LAST:event_bt_tambahActionPerformed

